export const getTypeColors = (color: string) => {
    if (color === "rgba(236, 205, 136, 1)") {
      return "born";
    }
    if (color === "rgba(236, 176, 136, 1)") {
      return "orange";
    }
    if (color === "rgba(231, 227, 207, 1)") {
      return "gray";
    }
    if (color === "rgba(197, 104, 27, 1)") {
      return "red";
    }
  };
